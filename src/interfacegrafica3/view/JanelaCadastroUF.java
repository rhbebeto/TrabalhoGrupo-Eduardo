    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed
        // TODO add your handling code here:
        //abasteci o objeto com os dados da tela
        int id = Integer.parseInt(txtId.getText());
        Uf uf = new Uf();
        uf.setNome(txtNomeEstado.getText());
        uf.setSigla(txtUF.getText());
        uf.setId(id);
        //inserir pessoa no banco de dados
        UfRepository ufRepository = new UfRepository();
        boolean retornoBanco = false;
        if(Integer.parseInt(txtId.getText()) == 0){
            //inserir
            retornoBanco = ufRepository.inserir(
                janelaPrincipal.conexaoMySQL.connection, 
                uf);
        }else{
            //atualizar
            retornoBanco = ufRepository.atualizar(
                janelaPrincipal.conexaoMySQL.connection, 
                uf);
        }       
        if(retornoBanco){
            JOptionPane.showMessageDialog(
                    this,
                    "Cadastro atualizado com sucesso!",
                    "Tela de cadastro",
                    JOptionPane.INFORMATION_MESSAGE
            );
            //limpar a janela
            limparJanela();
        } 
    }//GEN-LAST:event_btnGravarActionPerformed

